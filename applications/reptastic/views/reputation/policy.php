        <h3>Reputation Decay</h3>
        <p>After two full weeks of absence from raiding, Shadow Rep will decay at a rate of 25% per week. Decay will stop once either a main character or alternate character has attended a full raid. Attendance is recorded at the <em>END</em> of a raid, so a character must be present for one full raid every two weeks to avoid decay.</p>
        <p>Decay is processed automatically, beginning exactly two weeks after the character last attended a raid. In the interest of fairness and uniformity, no reasons will prevent decay &ndash; <em>the only way to avoid decay is by attending a raid.</em></p>
        <p>However, reputation is gained quickly and will be restored in very few raids after returning. Decay will take about 6 full weeks of absence from raiding to deplete a character's shadow reputation pool. Decay is not processed during US major holiday weeks (i.e. &ndash; New Years, Memorial Day, Independence Day, Labor Day, Thanksgiving and Christmas.)</p>
        
        <h3>Spending Shadow Reputation</h3>
        <p>Shadow Reputation points are spent only in guild sponsored raids (PVE.) Reputation is spent only when gear is won, not when requesting to roll.</p>
        <p>When rolling on gear, those rolling for their <strong>primary spec</strong> (see definition below) will be given first priority. If no one is rolling on gear for their primary spec, a roll will be called for offspec, at which time anyone able to use the item may roll.</p>
        <p><strong>Primary Spec: </strong>Your primary spec is whatever you are the majority of the time when raiding. If someone changes spec for a night or two to help the raid, they may roll on their previous spec. For example, a shadow priest goes holy to fill in a healing spot. The shadow priest would have been a caster DPS had they not switched spec to help the raid, and the majority of the time the shadow priest is considered caster DPS, so the shadow priest may roll on caster gear that drops without waiting for the offset call.</p>
        <ul>
            <li>Rare and uncommon loot does <strong>NOT</strong> require reputation points and is distributed randomly.</li>
        	<li>Epic recipes and offspec items <strong>DO NOT</strong> cost Shadow Reputation points, but rather a 75g deposit to the guild bank.</li>
        	<li><strong>Item Enhancement Materials</strong> use a special price table for purchase with reputation or gold. (See section below)</li>
        	<li>All BoE epic patterns/gear will may be purchased with Shadow Reputation for <u>Main spec only</u> and must be equipped immediately. Any member who attended the raid may purchase a BoE for Offspec or alts at 25% of the auction house value, otherwise items will be sold for guild bank funds.</li>
        </ul>
        <p>In a change from the past, <em>all tier levels</em> have identical item cost! Cost is percent based, no maximum, and rounded down to the nearest whole number and is deducted from the appropriate tier pool.</p>
