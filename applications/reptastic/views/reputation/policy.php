        <h3>No-shows</h3>
        <p>When you sign up for a raid, we're counting on you to be there! Plans are made in advance based on the raid composition and assignments/strategies are prepared based on this group. If for any reason you can't make it, you should cancel your signup BEFORE the start of the raid!</p>
        <p>Members who sign up for a raid and <b>do not cancel or show up</b> will lose 20% of their total Shadow Reputation after the first incident. Every member is allowed one "Ooopsie!" per progression tier. Any 'No-shows' after this will result in a loss of Shadow Reputation.</p>
        <p>You are considered a 'No-show' if you sign up for a raid and do not show up within 2 hours of the scheduled start time. You may always cancel a raid signup up to the event start time using the in-game calendar, the mobile armory website, the standard armory website, or by emailing Smuffy directly on the guild forums. <b>Canceling a signup will not hurt you in any way.</b></p>
        
        <h3>Tentative Sign ups</h3>
        <p>If you are unsure you can attend a raid, use the <em>Tentative</em> sign up on the in-game calendar. This will show your interest without running a risk of losing Shadow Reputation if you are not able to cancel or show up.</p>
        <p>Tentative signups do not receive a signup bonus. However, if you don't show up there is no penalty either!</p>
        
