        <h3>Universal Reputation</h3>
        <p>Shadow Reputation is universal and spans all currently farmed tier content. Reputation is not used on old content. Old content is defined as any tier below the current level of guild progression.</p>
        
        <h3>New Members</h3>
        <p>When joining Nights, new members receive an initial reputation bonus of fifty (50) reputation to get them started. New members will gain reputation at the same rate as full members from that point on.</p>
        
        <h3>Main Characters</h3>
        <p>When joining the guild or attending the first raid, a member must declare a <em>Main Character</em>. There are several reasons for declaring a main, but the primary reason this is needed for raiding is to help with raid composition and recruiting.</p>
        <p>It is necessary to declare a main so the raid leaders know what roles are filled and which ones are needed for recruiting purposes. If a member constantly changes roles in the raid, this can lead to confusion in raid recruiting or cause other members to be sidelined because someone else has taken their role. So, for consistency and fairness, all members must declare a main when adopting a raider status.</p>
        <p>There are some caveats to raiding with an alternate character, and they are listed below:</p>
        <ul>
            <li>If a member brings an alternate character <strong>AT THE RAID LEADER'S REQUEST</strong>: you may roll on all spec appropriate gear and your repairs are covered by the guild bank.</li>
            <li>Members are only allowed to bring an alt <strong>AT THEIR OWN REQUEST</strong> if raid composition allows, and may roll on all spec appropriate gear ONLY if no other main characters need. In the past, we've often been able to work alts into the raids and will definitely continue to do so &ndash; <strong>as composition allows!</strong></li>
        </ul>
        
        <h3>Shared Point Pool</h3>
        <p>This addition allows a member to share their earned points between all characters and spend them on whichever character they prefer, at any time! A member's points are stored in their main's account on the website, and so all users <em>must</em> be registered for a website account. Also, the username should match the main character's name. If you need assistance with this, please mail Smark in game!</p>
        <p>Points are spent from the main character's point pool and item cost is the same, whether on a main or an alt. Alt's are always allowed to roll on items for their spec as if they were a main. Once the alt is in the raid, there is no distinguishing it from a main as far as loot distribution goes.</p>
        
        <h3>Tardiness</h3>
