        <h3>Guest Raider Policy</h3>
        <p>Any guest who attends a Nights sponsored raid will be given early reputation points upon joining the raid, except in repeat circumstances; however, the character *must* register on the website for an account in order to roll on loot.</p>
        <p>A guest who raids with Nights on more than one occassion will be able to accumulate reputation points, just as normal members (assuming the guest has registered on the website.) Guest raiders suffer the same point decay as Nights members and are not awarded Veteran Bonuses during progression jumps.</p>
        
        <h3>Final Key Points:</h3>
        <p>Getting gear for a character is a time consuming process. For the casual characters, this may be a very long process since they cannot attend every raid we schedule. However, they will gain points over time and that could give them the advantage in some situations when their loot does happen to drop. We must all keep in mind that we're in this together - we are a team and a family of friends and acquaintances who are here to have fun, not to bicker over loot and drops.</p>
        <p>It is fair that those who attend raids more frequently be given the opportunity to get their gear, as they have been putting in time to get that gear. It is also important that those coming for the first time or on a casual basis be given the opportunity to get their gear.</p>
        <p>For the casual characters, keep in mind that you may not get your loot the first time you go into an instance if it drops. But, also remember that those who are in the group with you and who have been going for weeks on end didn't get their loot the first time they went into the instance either. As a guild of friends and fellowships, I know we are all here to help one another out and to be understanding and not let gear come between our bonds and friendships we have formed.</p>
        <p>For the moderate to hardcore raiders, keep in mind that casual members may win loot over you from time to time with this system, but that those who save their reputation points will have a higher probability of winning gear when they want it.</p>
